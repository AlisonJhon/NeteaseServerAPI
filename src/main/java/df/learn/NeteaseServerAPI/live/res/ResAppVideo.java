package df.learn.NeteaseServerAPI.live.res;

public class ResAppVideo {
	private String video_name;
	private String orig_video_key;
	private String uid;
	private String vid;

	/**
	 * @return the video_name
	 */
	public String getVideo_name() {
		return video_name;
	}

	/**
	 * @return the orig_video_key
	 */
	public String getOrig_video_key() {
		return orig_video_key;
	}

	/**
	 * @return the uid
	 */
	public String getUid() {
		return uid;
	}

	/**
	 * @return the vid
	 */
	public String getVid() {
		return vid;
	}

	/**
	 * @param video_name
	 *            the video_name to set
	 */
	public void setVideo_name(String video_name) {
		this.video_name = video_name;
	}

	/**
	 * @param orig_video_key
	 *            the orig_video_key to set
	 */
	public void setOrig_video_key(String orig_video_key) {
		this.orig_video_key = orig_video_key;
	}

	/**
	 * @param uid
	 *            the uid to set
	 */
	public void setUid(String uid) {
		this.uid = uid;
	}

	/**
	 * @param vid
	 *            the vid to set
	 */
	public void setVid(String vid) {
		this.vid = vid;
	}

}
